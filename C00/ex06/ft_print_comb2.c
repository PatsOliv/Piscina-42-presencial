#include<unistd.h>

void	ft_print_comb2(void)
{
	char	c;
	int		num1;
	int		num2;

	num1 = 0;
	while (num1 <= 98)
	{
		num2 = num1 + 1;
		while (num2 <= 99)
		{
			c = ((num1 / 10) + '0');
			write(1, &c, 1);
			c = ((num1 % 10) + '0');
			write(1, &c, 1);
			write(1, " ", 1);
			c = ((num2 / 10) + '0');
			write(1, &c, 1);
			c = ((num2 % 10) + '0');
			write(1, &c, 1);
			if (num1 / 10 != 9 || num1 % 10 != 8)
				write(1, ", ", 2);
			num2++;
		}
		num1++;
	}
}
